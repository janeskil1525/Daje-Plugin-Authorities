sub load_authorities_license_pkey($self) {
    $self->app->log->debug('Daje::Controller::Super::AuthoritiesLicense::load_authorities__fkey ');
    $self->render_later;
    my ($companies_pkey, $users_pkey) = $self->jwt->companies_users_pkey(
         $self->req->headers->header('X-Token-Check')
    );
    my $pkey = $self->param('authorities_license_pkey');

    $self->app->log->debug($self->req->headers->header('X-Token-Check'));
    # my $setting = $self->param('setting');
    $self->v_authorities_license->load_authorities_license_pkey_p($pkey)->then(sub($result) {
        $self->render(json => $result->{data});
    })->catch(sub($err) {
        $self->app->log->error('Daje::Controller::Super::AuthoritiesLicense::load_authorities_license_pkey ' . $err);
        $self->render
    });
}

